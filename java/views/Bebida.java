    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          List<String> selectedValuesList = bebidasList.getSelectedValuesList();
    List<Productos> productosSeleccionados = new ArrayList<>();

    try {
        em.getTransaction().begin();
        for (String nombreProducto : selectedValuesList) {
            Productos producto = em.createQuery("SELECT p FROM Productos p WHERE p.nombreProducto = :nombre", Productos.class)
                                   .setParameter("nombre", nombreProducto)
                                   .getSingleResult();
            productosSeleccionados.add(producto);
        }
        em.getTransaction().commit();
    } catch (Exception e) {
        if (em.getTransaction().isActive()) {
            em.getTransaction().rollback();
        }
        e.printStackTrace();
    }

    Carrito.AñadirAlCarrito(productosSeleccionados);


    }//GEN-LAST:event_jButton1ActionPerformed

