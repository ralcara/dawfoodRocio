    private void cargarBebida() {
         try {
        em.getTransaction().begin();
        List<Productos> bebidas = em.createQuery("SELECT p FROM Productos p WHERE p.tipoId = 2", Productos.class)
                .getResultList();
        em.getTransaction().commit();

        bebidasListModel.clear();
        for (Productos bebida : bebidas) {
            bebidasListModel.addElement(bebida.getNombreProducto());
        }
    } catch (Exception e) {
        if (em.getTransaction().isActive()) {
            em.getTransaction().rollback();
        }
        e.printStackTrace();
    }
    }


